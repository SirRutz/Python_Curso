# 4 - Crie um programa para entrar com a base a altura de um retângulo e imprimir respectivamente o perímetro e a área correspondente.

base = int(input('digite a base do retangulo: '))
altura = int(input('agora, digite a altura: '))
print('O perímetro do retangulo é: ',2*base + 2*altura)
print('A área desse retangulo é',base*altura)