# Faça um programa que solicite dois números ao usuário (com decimais) Em seguida solicite que o usuário informe o resultado das quatro operações matemáticas ( subtração, multiplicação e divisão)

numero1 = float(input('Insira um número: ')) 
numero2 = float(input('Insira outro número: '))

print('A adição desses números é: ', numero1 + numero2)
print('A subtração desses números é: ', numero1 - numero2)
print('A multiplicação desses números é: ', numero1 * numero2)
print('A divisão desses números é: ', numero1 / numero2)