# Elabore um algoritmo que dada a idade de um nadador classifique-o em uma das seguintes categorias:

idade = int(input('Digite a sua idade: '))
  
if idade <=7 >=5:
    print('Infantil A') 
elif idade <=11 >=8:
    print('Infantil B')
elif idade <=13 >=12:
    print('Juvenil A')
elif idade <=17 >=14:
    print('Juvenil B')
elif idade >18:
    print('Adultos')
    
    
